var number = 100;

// while loop will continue as long as the condition is TRUE
while (number <= 300) {
  console.log("AGAIN " + number);

  number += 1;
}

// for loop is the same pieces as while loop but in a different syntax
for (var number = 100; number <= 300; number += 1) {
  console.log("FOR AGAIN " + number);
}
